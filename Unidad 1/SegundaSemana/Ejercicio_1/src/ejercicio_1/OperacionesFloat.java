package ejercicio_1;

public class OperacionesFloat {
    
    public static float addition(float number_1, float number_2) {
        return number_1 + number_2;
    }
    public static float addition(float number_1, float number_2,float number_3) {
        return number_1 + number_2 + number_3;
    }
    public static float addition(float number_1, float number_2, float number_3, float number_4) {
        return number_1 + number_2 + number_3 + number_4;
    }
}
