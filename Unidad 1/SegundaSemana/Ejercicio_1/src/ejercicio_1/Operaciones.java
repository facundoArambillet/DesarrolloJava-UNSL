package ejercicio_1;

public class Operaciones {
    
    public static int addition(int number_1, int number_2) {
        return number_1 + number_2;
    }
    public static int addition(int number_1, int number_2,int number_3) {
        return number_1 + number_2 + number_3;
    }
    public static int addition(int number_1, int number_2, int number_3, int number_4) {
        return number_1 + number_2 + number_3 + number_4;
    }

}
