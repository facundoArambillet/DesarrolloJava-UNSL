package utilidades;

public class Operaciones {
    
    public static float addition(float number_1, float number_2) {
        return number_1 + number_2;
    }
    public static float subtraction(float number_1, float number_2) {
        return number_1 - number_2;
    }
    public static float multiplication(float number_1, float number_2) {
        return number_1 * number_2;
    }
    public static float division(float number_1, float number_2) {
        return number_1 / number_2;
    }
}
